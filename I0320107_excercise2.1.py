#tipe data Boolean
print(True)
print(type(True))

#tipe data string
print("Ayo belajar Python")
print('Belajar Python Sangat Mudah')
print(type("Ayo belajar Python"))

#tipe data Integer
print(20)
print(type(20))

#tipe data Float
print(3.14)
print(type(3.14))

#tipe data complex
print(5j)
